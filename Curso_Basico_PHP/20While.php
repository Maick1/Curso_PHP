<?php
//Estuctura repetitivas while (mientras)

$c=1;
while($c<=20){
    echo$c."<br>";
    $c++;
}

echo"<h1>Decremento</h1>";
$c=20;
while($c>=1){
    echo$c."<br>";
    $c--;
}

