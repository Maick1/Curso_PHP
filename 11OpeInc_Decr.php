<?php
$numero = 10;
$numero2 = 10;

#preincremento, primero incrementa luego la acciion
echo ++$numero;
echo "<br>";

#postincremento primero realiza la accion,
#luego incrementa
echo $numero++;
echo "<br>";


#predecremento, primero incrementa luego la acciion
echo --$numero2;
echo "<br>";

#postdecremento primero realiza la accion,
#luego incrementa
echo $numero2--;